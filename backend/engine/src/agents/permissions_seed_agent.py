def get_database_url():
    """Get database URL from environment."""
    return os.environ.get(
        "DATABASE_URL",
        "postgresql://postgres:postgres@localhost:5432/taskdb"
    )


def hash_password(password: str) -> str:
    """Hash password using bcrypt."""
    if HAS_BCRYPT:
        return bcrypt.hashpw(password.encode(), bcrypt.gensalt(12)).decode()
    # Fallback - NOT SECURE, only for development
    import hashlib
    return f"sha256:{hashlib.sha256(password.encode()).hexdigest()}"


def seed_with_sqlalchemy():
    """Seed permissions using SQLAlchemy."""
    database_url = get_database_url()
    engine = create_engine(database_url)
    Session = sessionmaker(bind=engine)
    session = Session()

    roles_created = 0
    permissions_created = 0
    admin_created = False

    try:
        # Check if tables exist
        result = session.execute(text("""
            SELECT table_name FROM information_schema.tables
            WHERE table_schema = 'public' AND table_name IN ('roles', 'permissions', 'users')
        """))
        existing_tables = {row[0] for row in result}

        # Seed permissions if table exists
        if 'permissions' in existing_tables:
            for perm in DEFAULT_PERMISSIONS:
                existing = session.execute(
                    text("SELECT id FROM permissions WHERE name = :name"),
                    {"name": perm["name"]}
                ).fetchone()

                if not existing:
                    session.execute(
                        text("INSERT INTO permissions (name, description) VALUES (:name, :desc)"),
                        {"name": perm["name"], "desc": perm["description"]}
                    )
                    permissions_created += 1

        # Seed roles if table exists
        if 'roles' in existing_tables:
            for role_name, role_data in DEFAULT_ROLES.items():
                existing = session.execute(
                    text("SELECT id FROM roles WHERE name = :name"),
                    {"name": role_name}
                ).fetchone()

                if not existing:
                    session.execute(
                        text("INSERT INTO roles (name, description) VALUES (:name, :desc)"),
                        {"name": role_name, "desc": role_data["description"]}
                    )
                    roles_created += 1

        # Create admin user if users table exists
        if 'users' in existing_tables:
            admin_email = os.environ.get("ADMIN_EMAIL", "admin@example.com")
            admin_password = os.environ.get("ADMIN_PASSWORD", "admin123!")

            existing_admin = session.execute(
                text("SELECT id FROM users WHERE email = :email"),
                {"email": admin_email}
            ).fetchone()

            if not existing_admin:
                hashed_pw = hash_password(admin_password)
                session.execute(
                    text("""
                        INSERT INTO users (email, password, role, name, is_active)
                        VALUES (:email, :password, 'admin', 'Administrator', true)
                    """),
                    {"email": admin_email, "password": hashed_pw}
                )
                admin_created = True
                print(f"  Created admin user: {admin_email}")

        session.commit()

    except Exception as e:
        session.rollback()
        print(f"  Error: {e}")
        raise
    finally:
        session.close()

    return {
        "success": True,
        "roles": roles_created,
        "permissions": permissions_created,
        "admin_created": admin_created,
    }


