"""
PermissionsSeedAgent - Seeds the database with roles, permissions, and admin user.

This agent runs after AUTH_SETUP_COMPLETE to ensure:
1. Default roles exist (admin, user, guest)
2. Default permissions are assigned to roles
3. Admin user is created with proper credentials
4. Initial permission matrix is established
"""

import json
import secrets
from pathlib import Path
from typing import Any, Optional

from src.agents.autonomous_base import AutonomousAgent
from src.mind.event_bus import Event, EventType


class PermissionsSeedAgent(AutonomousAgent):
    """Agent that seeds roles, permissions, and creates admin user."""

    subscribed_events = [
        EventType.AUTH_SETUP_COMPLETE,
        EventType.DATABASE_SCHEMA_GENERATED,
    ]

    def __init__(
        self,
        name: str = "PermissionsSeed",
        event_bus: Any = None,
        shared_state: Any = None,
        working_dir: str = ".",
        **kwargs,
    ):
        super().__init__(
            name=name,
            event_bus=event_bus,
            shared_state=shared_state,
            working_dir=working_dir,
            **kwargs,
        )
        self.working_dir = Path(working_dir)
        self._seeded = False
        self._auth_complete = False
        self._db_ready = False

    async def should_act(self, events: list[Event]) -> bool:
        """Act when auth is complete or database schema is generated."""
        if self._seeded:
            return False

        for event in events:
            if event.type == EventType.AUTH_SETUP_COMPLETE:
                self._auth_complete = True
                return True
            if event.type == EventType.DATABASE_SCHEMA_GENERATED:
                self._db_ready = True
                # Only act if auth is also complete
                if self._auth_complete:
                    return True

        return False

    async def act(self, events: list[Event]) -> None:
        """Seed roles, permissions, and admin user."""
        self.logger.info("permissions_seed_starting", working_dir=str(self.working_dir))

        try:
            # 1. Generate permissions seed script if it doesn't exist
            seed_script = await self._ensure_permissions_seed_script()

            if seed_script:
                # 2. Run the permissions seed
                result = await self._run_permissions_seed(seed_script)

                if result["success"]:
                    self._seeded = True
                    self.logger.info(
                        "permissions_seed_complete",
                        roles_created=result.get("roles", 0),
                        permissions_created=result.get("permissions", 0),
                        admin_created=result.get("admin_created", False),
                    )

                    # Publish completion event
                    await self._publish_event(
                        EventType.GENERATION_COMPLETE,
                        data={
                            "component": "permissions_seed",
                            "success": True,
                            "admin_created": result.get("admin_created", False),
                            "roles": result.get("roles", []),
                        },
                    )
                else:
                    self.logger.warning(
                        "permissions_seed_failed",
                        error=result.get("error", "Unknown error"),
                    )
            else:
                # Create a basic permissions structure in env
                await self._create_default_admin_env()
                self._seeded = True

        except Exception as e:
            self.logger.error("permissions_seed_error", error=str(e))

    async def _ensure_permissions_seed_script(self) -> Optional[Path]:
        """Ensure a permissions seed script exists, create if needed."""

        # Check for existing seed scripts
        python_seed = self.working_dir / "scripts" / "seed_permissions.py"
        ts_seed = self.working_dir / "prisma" / "seed-permissions.ts"

        if python_seed.exists():
            return python_seed
        if ts_seed.exists():
            return ts_seed

        # Create Python permissions seed script
        scripts_dir = self.working_dir / "scripts"
        scripts_dir.mkdir(exist_ok=True)

        seed_content = self._generate_permissions_seed_script()
        python_seed.write_text(seed_content)

        self.logger.info("permissions_seed_script_created", path=str(python_seed))
        return python_seed

