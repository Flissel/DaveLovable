    async def _create_default_admin_env(self) -> None:
        """Add default admin credentials to .env if not present."""
        env_file = self.working_dir / ".env"

        if not env_file.exists():
            return

        content = env_file.read_text()
        additions = []

        if "ADMIN_EMAIL" not in content:
            additions.append("ADMIN_EMAIL=admin@example.com")
        if "ADMIN_PASSWORD" not in content:
            # Generate a secure random password
            password = secrets.token_urlsafe(12)
            additions.append(f"ADMIN_PASSWORD={password}")
            self.logger.info("admin_password_generated",
                           note="Check .env for ADMIN_PASSWORD")

        if additions:
            with open(env_file, "a") as f:
                f.write("\n# Admin User (auto-generated)\n")
                f.write("\n".join(additions))
                f.write("\n")

