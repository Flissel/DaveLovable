    # Write permissions config
    perms_file = config_dir / "permissions.json"
    perms_file.write_text(json.dumps(DEFAULT_PERMISSIONS, indent=2))

    # Create admin credentials (for development only)
    admin_email = os.environ.get("ADMIN_EMAIL", "admin@example.com")
    admin_password = os.environ.get("ADMIN_PASSWORD", "admin123!")

    admin_file = config_dir / "admin.json"
    admin_file.write_text(json.dumps({
        "email": admin_email,
        "password_hash": hash_password(admin_password),
        "role": "admin",
    }, indent=2))

    return {
        "success": True,
        "roles": len(DEFAULT_ROLES),
        "permissions": len(DEFAULT_PERMISSIONS),
        "admin_created": True,
        "config_dir": str(config_dir),
    }


