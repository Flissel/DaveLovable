async def ollama_generate(
    prompt: str,
    model: str = "codellama:34b",
    context: Optional[str] = None,
    working_dir: Optional[str] = None,
) -> OllamaGenerationResult:
    """
    Convenience function for quick code generation.

    Args:
        prompt: What to generate
        model: Ollama model to use
        context: Additional context
        working_dir: Directory for file output

    Returns:
        OllamaGenerationResult
    """
    tool = OllamaTool(model=model, working_dir=working_dir)
    return await tool.execute(prompt, context)


async def check_ollama_available(
    base_url: str = "http://localhost:11434",
) -> tuple[bool, list[str]]:
    """
    Check if Ollama is available and list models.

    Returns:
        (is_available, list_of_models)
    """
    try:
        import aiohttp

        async with aiohttp.ClientSession() as session:
            async with session.get(
                f"{base_url}/api/tags",
                timeout=aiohttp.ClientTimeout(total=5),
            ) as resp:
                if resp.status == 200:
                    data = await resp.json()
                    models = [m.get("name", "") for m in data.get("models", [])]
                    return True, models
                return False, []
    except Exception:
        return False, []
