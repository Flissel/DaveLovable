"""
CRUD Endpoint Detector - Detects CRUD endpoints from frontend code.

Analyzes React/Vue/Svelte components to find:
- API calls (fetch, axios, react-query, tanstack-query)
- Form submissions
- Button actions for CRUD operations

Used by E2EIntegrationTeamAgent to discover testable endpoints.
"""

