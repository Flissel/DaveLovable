# =============================================================================
# Data Classes
# =============================================================================

@dataclass
class CRUDEndpoint:
    """Detected CRUD endpoint from frontend code."""
    entity: str              # e.g., "User", "Order", "Product"
    operation: str           # "create", "read", "update", "delete"
    http_method: str         # GET, POST, PUT, PATCH, DELETE
    endpoint_path: str       # "/api/users", "/api/orders/:id"
    source_file: str         # Component file that uses this endpoint
    form_fields: list[str] = field(default_factory=list)  # Form field names
    auth_required: bool = False  # Whether auth header is used
    line_number: int = 0     # Line in source file

    def to_dict(self) -> dict:
        return {
            "entity": self.entity,
            "operation": self.operation,
            "http_method": self.http_method,
            "endpoint_path": self.endpoint_path,
            "source_file": self.source_file,
            "form_fields": self.form_fields,
            "auth_required": self.auth_required,
        }


@dataclass
class DetectionResult:
    """Result of CRUD endpoint detection."""
    endpoints: list[CRUDEndpoint] = field(default_factory=list)
    entities: list[str] = field(default_factory=list)
    files_scanned: int = 0
    errors: list[str] = field(default_factory=list)

    @property
    def has_crud(self) -> bool:
        """Check if any CRUD operations were detected."""
        return len(self.endpoints) > 0


